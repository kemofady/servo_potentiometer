#ifndef STD_TYPES_H_
#define STD_TYPES_H_

typedef unsigned char u8;
typedef unsigned short int u16;
typedef unsigned long int u32;

typedef signed char s8;
typedef signed short int s16;
typedef signed long int s32;

typedef float f32;
typedef double f64;

#define STD_OK  0
#define STD_NOK 1

#define STD_ON  0
#define STD_OFF 1

#define NULL '\0'

#endif /* STD_TYPES_H_ */
